package Lab1;

public class Swapping {
	public static void main(String[]args) {
		int tom=3;
		int jerry=5;
		System.out.println("Tom has" + tom + "apples");
		System.out.println("Jerry has" + jerry + "apples");
		int swap = tom;
		tom = jerry;
		jerry = swap;
		System.out.println("\nAfter this:");
		System.out.println("Tom has" + tom + "apples");
		System.out.println("Jerry has" + jerry + "apples");
		
		 
	}
	
	
	 



}
